print(1 > 2)  # Uno mayor que Dos False
print(1 < 2)  # Uno Menor que Dos True
print(1 >= 2)  # Uno Mayor o Igual A Dos False
print(1 <= 2)  # Uno Menor o Igual A Dos True
print(2 >= 2)  # Dos Mayor o Igual a Dos True
print(2 == 2)  # Dos Igual Absoluto a Dos True
print(2 == 3)  # Dos Igual Absoluto a Tres False
print(2 == "2")  # Dos Igual Absoluto a String "2" False
print(2 != "2")  # Dos es No Igual Al String "2" True
print(2 != 2)  # Dos es No Igual a Dos False
